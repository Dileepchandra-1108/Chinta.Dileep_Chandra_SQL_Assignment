-- Question 5 (Marks: 3)
-- Objective: Identify seat occupancy by comparing the number of boarding passes (occupied seats) to the total seats available on an aircraft.
-- Determine Occupancy per Aircraft
-- Question Template: Display following columns flight_id, flight_no, aircraft_code, total_seats, occupied_seats, occupancy_rate_percentage

select f.flight_id,f.flight_no,f.aircraft_code,ts.total_seats,os.occupied_seats,
round((os.occupied_seats * 100.0) / ts.total_seats, 2) as occupancy_rate_percentage
from flights f
join (select aircraft_code,
count(seat_no) as total_seats
from seats
group by aircraft_code) ts on f.aircraft_code = ts.aircraft_code
left join (select flight_id,
count(ticket_no) as occupied_seats
from ticket_flights
group by flight_id) os on f.flight_id = os.flight_id
limit 100;

