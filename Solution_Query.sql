-- Question 9 (Marks: 3)
-- Objective: Count how many flights depart from and arrive at each airport to assess airport activity levels.
-- Flight Count per Airport (Arrival)
-- Question Template: Display following columns arrival_airport, arrival_flights

select airport,
sum(departure_count) as departure_flights,
sum(arrival_count) as arrival_flights
from 
    (select departure_airport as airport,
	count(*) as departure_count,0 as arrival_count
    from flights
    group by departure_airport
    union all
    select arrival_airport as airport,
	0 as departure_count,
	count(*) as arrival_count
    from flights
    group by arrival_airport) as airport_counts
group by airport
order by (sum(departure_count) + sum(arrival_count)) desc;


